public class Ex2 {
    public static void main(String[] args) {
        //단순 for 문
        int i;
        int[] a = {0,1,2,3}; //4크기인 배열 a 생성
        for (i=0; i<4; i++ ){ // i가 0,1,2,3 까지 반복 (4보다 작을떄 까지)
            System.out.print(a[i] + " "); //a배열의 0번째, 1번째 ,2번쨰 ,3번째 값이 차례대로 출력됨
        }
    }
}
